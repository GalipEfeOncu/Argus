#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { compile } from 'json-schema-to-typescript';
import openapiTS, { astToString } from 'openapi-typescript';

const repositoryRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const contractsDirectory = join(repositoryRoot, 'contracts');
const generatedDirectory = join(repositoryRoot, 'src', 'types', 'generated');
const python = process.platform === 'win32'
  ? join(repositoryRoot, 'backend', '.venv', 'Scripts', 'python.exe')
  : join(repositoryRoot, 'backend', '.venv', 'bin', 'python3');
const generatedMarker = `/*\n * Generated by scripts/generate-contracts.mjs.\n * Sources: canonical Pydantic adapters and FastAPI OpenAPI.\n * Do not edit this file manually.\n */\n\n`;

function writeGenerated(path, contents) {
  return writeFile(path, contents.endsWith('\n') ? contents : `${contents}\n`, 'utf8');
}

async function generateSchemaTypes(schemaFile, outputFile, typeName) {
  const schema = JSON.parse(await readFile(join(contractsDirectory, schemaFile), 'utf8'));
  const types = await compile(schema, typeName, {
    bannerComment: generatedMarker,
    unknownAny: true,
  });
  await writeGenerated(join(generatedDirectory, outputFile), types);
}

async function main() {
  await mkdir(generatedDirectory, { recursive: true });
  execFileSync(python, [join(repositoryRoot, 'backend', 'scripts', 'export_contracts.py')], {
    cwd: repositoryRoot,
    stdio: 'inherit',
  });

  await generateSchemaTypes('session-events.schema.json', 'session-events.ts', 'ArgusSessionEvent');
  await generateSchemaTypes('session-commands.schema.json', 'session-commands.ts', 'ArgusSessionCommand');

  const openapi = JSON.parse(await readFile(join(contractsDirectory, 'openapi.json'), 'utf8'));
  const restAst = await openapiTS(openapi, { alphabetize: true });
  await writeGenerated(join(generatedDirectory, 'rest.ts'), `${generatedMarker}${astToString(restAst)}`);
}

await main();
