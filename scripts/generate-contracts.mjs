#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { lstat, mkdir, readFile, realpath, writeFile } from 'node:fs/promises';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { compile } from 'json-schema-to-typescript';
import openapiTS, { astToString } from 'openapi-typescript';

const repositoryRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const python = process.platform === 'win32'
  ? join(repositoryRoot, 'backend', '.venv', 'Scripts', 'python.exe')
  : join(repositoryRoot, 'backend', '.venv', 'bin', 'python3');
const generatedMarker = `/*\n * Generated by scripts/generate-contracts.mjs.\n * Sources: canonical Pydantic adapters and FastAPI OpenAPI.\n * Do not edit this file manually.\n */\n\n`;

function hasParentTraversal(path) {
  return path.split(/[\\/]+/).includes('..');
}

async function outputRootFromArguments(arguments_) {
  const workspaceRoot = await realpath(repositoryRoot);

  if (arguments_.length !== 0 && (arguments_.length !== 2 || arguments_[0] !== '--output-dir')) {
    throw new Error('Usage: generate-contracts.mjs [--output-dir <directory>]');
  }

  const requestedOutputRoot = arguments_.length === 0 ? workspaceRoot : arguments_[1];

  if (hasParentTraversal(requestedOutputRoot)) {
    throw new Error('Generated contract output directory must not contain parent traversal.');
  }

  const outputRoot = resolve(requestedOutputRoot);
  const outputRelativePath = relative(workspaceRoot, outputRoot);
  if (outputRelativePath === '..' || outputRelativePath.startsWith(`..${process.platform === 'win32' ? '\\' : '/'}`)) {
    throw new Error('Generated contract output directory must stay within the repository workspace.');
  }

  let inspectedPath = workspaceRoot;
  for (const segment of outputRelativePath.split(/[\\/]+/).filter(Boolean)) {
    inspectedPath = join(inspectedPath, segment);
    try {
      const metadata = await lstat(inspectedPath);
      if (metadata.isSymbolicLink()) {
        throw new Error('Generated contract output directory must not traverse symbolic links.');
      }
      if (!metadata.isDirectory()) {
        throw new Error('Generated contract output path must be a directory.');
      }
    } catch (error) {
      if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
        break;
      }
      throw error;
    }
  }

  return outputRoot;
}

function writeGenerated(path, contents) {
  return writeFile(path, contents.endsWith('\n') ? contents : `${contents}\n`, 'utf8');
}

async function generateSchemaTypes(contractsDirectory, generatedDirectory, schemaFile, outputFile, typeName) {
  const schema = JSON.parse(await readFile(join(contractsDirectory, schemaFile), 'utf8'));
  const types = await compile(schema, typeName, {
    bannerComment: generatedMarker,
    unknownAny: true,
  });
  await writeGenerated(join(generatedDirectory, outputFile), types);
}

async function main() {
  const outputRoot = await outputRootFromArguments(process.argv.slice(2));
  const contractsDirectory = join(outputRoot, 'contracts');
  const generatedDirectory = join(outputRoot, 'src', 'types', 'generated');

  await mkdir(generatedDirectory, { recursive: true });
  execFileSync(python, [join(repositoryRoot, 'backend', 'scripts', 'export_contracts.py'), '--output-dir', contractsDirectory], {
    cwd: repositoryRoot,
    stdio: 'inherit',
  });

  await generateSchemaTypes(contractsDirectory, generatedDirectory, 'session-events.schema.json', 'session-events.ts', 'ArgusSessionEvent');
  await generateSchemaTypes(contractsDirectory, generatedDirectory, 'session-commands.schema.json', 'session-commands.ts', 'ArgusSessionCommand');

  const openapi = JSON.parse(await readFile(join(contractsDirectory, 'openapi.json'), 'utf8'));
  const restAst = await openapiTS(openapi, { alphabetize: true });
  await writeGenerated(join(generatedDirectory, 'rest.ts'), `${generatedMarker}${astToString(restAst)}`);
}

await main();
